const nodeById = new Map(SEMIOTIC_ARCHITECTURE_NODES.map((node) => [node.id, node]))

/**
 * Expand an example's direct usage into its visible branch-to-trunk paths and
 * the implementation roots that support any highlighted node.
 */
export function architectureHighlight(profile) {
  if (!profile) return new Set()

  const highlighted = new Set(profile.uses)

  for (const id of [...highlighted]) {
    let current = nodeById.get(id)
    while (current?.parent) {
      highlighted.add(current.parent)
      current = nodeById.get(current.parent)
    }
  }

  for (const node of SEMIOTIC_ARCHITECTURE_NODES) {
    if (node.supports?.some((target) => highlighted.has(target))) {
      highlighted.add(node.id)
    }
  }

  if ([...highlighted].some((id) => nodeById.get(id)?.layer === "root")) {
    highlighted.add("semiotic-internals")
  }

  return highlighted
}

export function architectureNode(id) {
  return nodeById.get(id)
}
