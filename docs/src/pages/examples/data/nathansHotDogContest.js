export function countLabel(value) {
  return Number(value).toLocaleString("en-US", {
    minimumFractionDigits: Number.isInteger(value) ? 0 : 1,
    maximumFractionDigits: 3,
  })
}

export function contestRate(row) {
  return row.duration ? row.count / row.duration : null
}

export function yearTime(year) {
  return (year - NATHANS_START_YEAR) * NATHANS_YEAR_MS
}

export function resultWithTime(row) {
  return {
    ...row,
    division: row.division || (row.gender === "Women" ? "Women's" : "Men's"),
    time: yearTime(row.year),
    value: row.count,
    rate: contestRate(row),
  }
}
